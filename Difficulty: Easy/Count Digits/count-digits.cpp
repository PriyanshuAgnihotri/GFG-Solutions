class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int ans =0;
        string temp = to_string(N);
        for(int i=0;i<temp.size();i++){
            int x= temp[i] - '0';
            if(x ==0)
            continue;
            if (N % x ==0)
            ans++;
        }
        return ans;
    }
};

